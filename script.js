const data = {
  specials: ['Backspace', 'Tab', 'Enter', 'CapsLock', 'ShiftLeft', 'ShiftRight', 'Delete', 'AltLeft', 'AltRight', 'ControlLeft', 'ControlRight', 'MetaLeft'],
  keyboardRows: keysArray,
}

class VirtualKeyboard {
  constructor() {
    this.isCapsLock = false
    this.isShiftLeft = false
    this.isShiftRight = false
    this.isDoubleShift = false
    this.case = 'caseDown'
    this.lang = 'en'
    this.textarea = document.createElement('textarea')
    this.keyboard = document.createElement('div')
    this.info = document.createElement('div')
    this.current = {
      element: null,
      code: null,
      event: null,
      char: null,
    }
  }

  init(a) {
    this.initInfo()
    this.initKeyboard(a)
    this.initLanguage()

    document.addEventListener('keyup', this.keyUpHandler.bind(this))
    document.addEventListener('keydown', this.keyDownHandler.bind(this))
    this.keyboard.addEventListener('mouseup', this.mouseUpHandler.bind(this))
    this.keyboard.addEventListener('mousedown', this.mouseDownHandler.bind(this))
  }

  initKeyboard(a) {
    const container = document.createElement('div')

    container.classList.add('container')
    this.textarea.classList.add('text')
    this.keyboard.classList.add('keyboard')

    this.keyboard.append(this.renderKeybord(a))
    container.append(this.textarea, this.keyboard, this.info)
    document.body.append(container)
  }

  initInfo() {
    const langInfo = document.createElement('p')
    langInfo.textContent = 'To change language press - "Ctrl" + "Alt"'

    const sysInfo = document.createElement('p')
    sysInfo.textContent = 'Made in Windows OS'

    this.info.append(langInfo, sysInfo)
  }

  initLanguage() {
    if (localStorage.lang === 'ru') {
      this.changeLang()
    }
  }

  renderKeybord(a) {
    const elements = document.createDocumentFragment()
    for (let i = 0; i < a.length; i++) {
      const row = document.createElement('div')
      row.classList.add('row')
      for (let k = 0; k < a[i].length; k++) {
        const keyDiv = document.createElement('div')
        keyDiv.classList.add('keyboard-key', 'key', a[i][k].classWide, a[i][k].className)

        const elemRu = document.createElement('span')
        elemRu.classList.add('ru', 'hidden')
        elemRu.insertAdjacentHTML('afterBegin', `<span class='caseDown hidden'>${a[i][k].ru.caseDown}</span>`)
        elemRu.insertAdjacentHTML('beforeEnd', `<span class='caseUp hidden'>${a[i][k].ru.caseUp}</span>`)
        keyDiv.appendChild(elemRu)

        const elemEn = document.createElement('span')
        elemEn.classList.add('en')
        elemEn.insertAdjacentHTML('afterBegin', `<span class='caseDown'>${a[i][k].en.caseDown}</span>`)
        elemEn.insertAdjacentHTML('beforeEnd', `<span class='caseUp hidden'>${a[i][k].en.caseUp}</span>`)
        keyDiv.appendChild(elemEn)

        row.appendChild(keyDiv)
      }

      elements.appendChild(row)
    }
    return elements
  }

  addActiveState() {
    this.current.element.classList.add('active')
  }

  removeActiveState() {
    this.current.element.classList.remove('active')
  }

  changeCase() {
    this.changeHidden()

    if (this.case == 'caseDown') {
      this.case = 'caseUp'
    } else {
      this.case = 'caseDown'
    }

    this.changeHidden()
  }

  changeLang() {
    this.changeHidden()

    if (this.lang == 'en') {
      this.lang = 'ru'
    } else {
      this.lang = 'en'
    }

    this.changeHidden()
    localStorage.setItem('lang', this.lang)
  }

  changeHidden() {
    const e = this.keyboard.querySelectorAll(`div>.${this.lang}`)
    for (let i = 0; i < e.length; i++) {
      e[i].classList.toggle('hidden')
      e[i].querySelectorAll(`span.${this.case}`)[0].classList.toggle('hidden')
    }
  }

  addCharHandler() {
    const e = this.textarea.value
    const position = this.textarea.selectionStart
    if (data.specials.includes(this.current.code)) {
      switch (this.current.code) {
        case 'Backspace':
          if (position > 0 && position <= e.length) {
            this.removeCharFromTextArea(e, position - 1)
          }
          break
        case 'Delete':
          if (position >= 0 && position <= e.length - 1) {
            this.removeCharFromTextArea(e, position)
          }
          break
        case 'Tab':
          this.current.char = '	'
          this.addCharToTextArea(e, position)
          break
        case 'Enter':
          this.current.char = '\n'
          this.addCharToTextArea(e, position)
          break
        case 'CapsLock':
          if (this.isCapsLock) {
            this.isCapsLock = false
            this.removeActiveState()
            this.changeCase()
          } else {
            this.isCapsLock = true
            this.addActiveState()
            this.changeCase()
          }
          break
        case 'ShiftLeft':
          if (!this.isShiftRight) {
            this.changeCase()
          } else {
            this.isDoubleShift = true
          }
          break
        case 'ShiftRight':
          if (!this.isShiftLeft) {
            this.changeCase()
          } else {
            this.isDoubleShift = true
          }
      }
    } else {
      this.addCharToTextArea(e, position)
    }

    if (this.current.event.ctrlKey && this.current.event.altKey) {
      this.changeLang()
    }
  }

  addCharToTextArea(e, position) {
    if (position >= 0 && position <= e.length) {
      this.textarea.value = e.slice(0, position) + this.current.char + e.slice(position, e.length),
      this.textarea.selectionStart = position + this.current.char.length
      this.textarea.selectionEnd = position + this.current.char.length
    } else {
      this.textarea.value += this.current.char
    }
  }

  removeCharFromTextArea(e, position) {
    e = e.slice(0, position) + e.slice(position + 1, e.length)
    this.textarea.value = e
    this.textarea.selectionStart = position
    this.textarea.selectionEnd = position
  }

  keyUpHandler(e) {
    const el = document.getElementsByClassName(e.code)[0]
    this.current.element = el.closest('div')

    if (this.current.code !== 'CapsLock') {
      this.removeActiveState()
    }

    if (e.code === 'ShiftLeft') {
      this.isShiftLeft = false
      this.changeCase()
      this.removeActiveState()
      if (this.isDoubleShift) {
        this.current.element = document.querySelector('.ShiftRight')
        this.removeActiveState()
        this.isShiftRight = false
        this.isDoubleShift = false
      }
    }
    if (e.code === 'ShiftRight') {
      this.isShiftRight = false
      this.changeCase()
      this.removeActiveState()
      if (this.isDoubleShift) {
        this.current.element = document.querySelector('.ShiftLeft')
        this.removeActiveState()
        this.isShiftLeft = false
        this.isDoubleShift = false
      }
    }
  }

  keyDownHandler(e) {
    e.preventDefault()
    this.current.event = e
    this.current.code = e.code;
    [this.current.element] = document.getElementsByClassName(e.code)

    if (this.current.code === 'ShiftLeft') {
      this.isShiftLeft = true
    }
    if (this.current.code === 'ShiftRight') {
      this.isShiftRight = true
    }

    this.addActiveState()
    this.current.char = this.current.element.querySelectorAll('span:not(.hidden)')[1].textContent
    this.addCharHandler()
  }

  mouseUpHandler(e) {
    this.current.event = e
    this.current.element = e.target.closest('div')
    this.current.code = this.current.element.classList[3]

    if (this.current.element && this.current.element.classList.contains('key') && this.current.code !== 'CapsLock') {
      this.removeActiveState()
    }

    if (this.current.code === 'ShiftLeft') {
      this.isShiftLeft = false
      this.changeCase()
      this.removeActiveState()
    }
    if (this.current.code === 'ShiftRight') {
      this.isShiftRight = false
      this.changeCase()
      this.removeActiveState()
    }
  }

  mouseDownHandler(e) {
    this.current.event = e
    this.current.element = e.target.closest('div')

    if (this.current.element.classList.contains('key')) {
      this.current.char = this.current.element.querySelectorAll('span:not(.hidden)')[1].textContent
      this.current.code = this.current.element.classList[3]
      this.addActiveState()
      this.addCharHandler()
    }
  }
}

window.onload = () => {
  const VKeyboard = new VirtualKeyboard()
  VKeyboard.init(data.keyboardRows)
}

